#include <stdio.h>
#include <stdint.h>
#include "gpio_api.h"

int test( void )
{
    uint32_t valueW, valueR;

    gpio_csr();

    gpio_init(~0x0, 0x0, 0x0, 0x0);
    valueW = 0x12345678;
    gpio_write(valueW);
    valueR = gpio_read();
    printf("value=0x%08X\n", valueR);
    return 0;
}

